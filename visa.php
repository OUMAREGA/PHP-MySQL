Vérifiez si vous êtes admissible et si vous pouvez présenter une demande en ligne



Si vous possédez un permis d’études ou un permis de travail valide et que vous envisagez de quitter le Canada et d’y revenir, vous pouvez présenter une nouvelle demande de visa de visiteur avant de quitter le Canada.

1. Obtenez la trousse de demande

La trousse de demande comprend un guide et tous les formulaires que vous devez remplir.

Lisez attentivement le guide avant de remplir la demande. Les frais de traitement de la demande ne sont pas remboursables. Assurez-vous donc d’être admissible à l’obtention d’un visa de résident temporaire (visiteur) avant de présenter une demande.

Chaque demandeur doit présenter un formulaire de demande dûment rempli. Si un demandeur est âgé de moins de 18 ans, l’un de ses parents ou tuteurs doit signer le formulaire.

Télécharger la trousse de demande.

2. Payez les frais de traitement de votre demande

Dans la plupart des pays, les frais de traitement des demandes doivent être payés en dollars canadiens. Consultez l’outil de paiement des frais pour en savoir plus et connaître les modes de paiement que vous pouvez utiliser.

Si vous présentez une demande papier à un CRDV, vous devrez également acquitter les frais de service de ce centre. Les frais de traitement de votre demande ne comprennent pas les frais de service du CRDV.

3. Présentez votre demande

Les familles doivent envoyer leurs demandes individuelles dans une seule trousse. Le demandeur principal doit envoyer cette trousse à l’endroit où leur demande sera traitée.
